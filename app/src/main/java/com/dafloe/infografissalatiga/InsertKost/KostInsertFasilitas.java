package com.dafloe.infografissalatiga.InsertKost;

public class KostInsertFasilitas {

    public String Kasur;
    public String Wifi;
    public String Meja;
    public String Lemari;
    public String Kamar_Mandi;

    public KostInsertFasilitas(String kasur, String wifi, String meja, String lemari, String kamar_Mandi) {
        Kasur = kasur;
        Wifi = wifi;
        Meja = meja;
        Lemari = lemari;
        this.Kamar_Mandi = kamar_Mandi;
    }
}
